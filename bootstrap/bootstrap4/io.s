


#===========================================================================
# R0: Filename (zero terminated)
# R1: Mode (0 = read, 1 = rw+create)
#===========================================================================
:_open
	%arg filename, mode
	eq @mode, @OPEN_READ
	jump? .read
# read/write
	mov @tmp1, @O_RDWR
	or @tmp1, @O_CREAT
	or @tmp1, @O_TRUNC
	jump .open
# readonly
.read
	mov @tmp1, @O_RDONLY
.open
	%call :syscall3, @SC_OPEN, @filename, @tmp1
	mov @tmp0, r0
	add @tmp0, $1
	eq @tmp0, $0
	%call? :fatal, :open_error
	%ret r0

:open_error
	ds "Failed to open file"
